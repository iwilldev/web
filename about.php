<h3>A brief history:</h3>
<p>
In middle school, I started messing with javascript and html, but had no idea what I was doing. 
I would edit the code inside of microsoft word on the school computers and ran the code through an html tester. 
Made a heavily modified version of a snake game I found on the internet and would play it because all the game websites were blocked.
</p>

<p>
Started making real video games about a year later using the <a href= "https://scratch.mit.edu">Scratch</a> website, around age 12, after going to a game development club over the summer for "gifted students"... 
Some students in the club used the GameMaker: Studio engine, however I hated the workflow for that engine, and didn't understand object oriented programming yet, so I stayed with Scratch.
For some reason, I really wanted to make my own operating system in middle school. From there, I stumbled upon <a href="https://wiki.osdev.org/">wiki.osdev.org</a> but had little to no idea what I was reading. 
I read through the wiki for around an hour per day when I was supposed to be doing my homework, later discovering <a href="https://www.linuxfromscratch.org/">Linux-From-Scratch</a>.
I never did much with these websites besides nod my head while reading, as if I knew what I was reading.
</p>
<p>
In high school, I made a bunch of video games on my TI-84 CE, writing code on bus rides to and from school.
Some of my calculator projects included a maze-based puzzle game, and anthill simulator, and a snake game.
I made my first text-based game with batch files on windows. It that was configurable via directories and .ini files.
At home, I started looking up coding languages and discovered Python.
I later rewrote it in Python, using a .ini file configuration library and custom objects for items, enemies, and locations.
While the Python version of the game worked, I never really finished it.
</p>
<p>
At the same time, got pretty serious on Scratch site, making physics engines and other such games. I began doing polished work. (For the time)
A lot of my best Scratch projects never saw the light of day, mostly because it was unfinished and I was afraid of people stealing my marvelous code.
I later joined a game development group called Amber Studios off of Scratch and helped make a bunch more games with the Godot game engine.
I learned through this work that I hated doing 3d modeling for video games, but loved solid modeling CAD software
I was decent at making 2d art but I didn't enjoy it much. It was just a means to an end for me.
</p>

<p>
My second year of high school I discovered Linux.
</p>
I managed to get my hands on an old Core2 Duo PC, and I installed Linux Mint.
The Covid-19 shutdowns happened midway through this year and I was given way too much time to myself. 
Because of this, I decided it would be a great idea to switch my laptop which I used for schoolwork over to a Linux distribution as well.
I started with Debian on my laptop, but my BIOS wouldn't allow GRUB to rewrite my EFI variables. I had to manually intervene to get it to install.
The Debian install lasted until I got a kernel update. Upon which the old kernel was deleted, and the new kernel was saved to the wrong folder as I did not change the install configuration.
And so I was left with a non-booting OS.
</p>
<p>
I put my foot down and installed ArchLinux at 2am in the morning with Systemd-Boot instead of the Grub bootloader.
I still use the same install on the same exact hard drive to this day.
</p>

